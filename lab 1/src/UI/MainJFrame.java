    private void display() {
        //display person class attributes
        viewName.setText(this.person.getFirstName());   
        viewCollegeName.setText(this.person.getCollegeName());
        viewLastName.setText(this.person.getLastName());
        viewNUID.setText(this.person.getnUID());
        
        //display currentAddress of person.
        viewCStreetName.setText(this.person.getCurrentAddress().getStreetName());
        viewCAptNo.setText(this.person.getCurrentAddress().getAptNo());
        viewCCityState.setText(this.person.getCurrentAddress().getCityState());
        viewCZipCode.setText(this.person.getCurrentAddress().getZipCode());
        
        //display permanentAddress of person.
        viewPStreetName.setText(this.person.getPermanentAddress().getStreetName());
        viewPAptNo.setText(this.person.getPermanentAddress().getAptNo());
        viewPCityState.setText(this.person.getPermanentAddress().getCityState());
        viewPZipCode.setText(this.person.getPermanentAddress().getZipCode());
        
        viewPPhNo.setText(this.person.getPersonalContact().getPhoneNumber());
        viewPEmail.setText(this.person.getPersonalContact().getEmail());
        
        viewOPhNo.setText(this.person.getPersonalContact().getPhoneNumber());
        viewOEmail.setText(this.person.getPersonalContact().getEmail());
        
    }

