    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String firstName = fieldFirstName.getText();
        String lastName = fieldLastName.getText();
        String collegeName = fieldCollegeName.getText();
        String nUID = fieldNUID.getText();
        
        String cStreet = fieldcStreet.getText();
        String cAptNo = fieldcAptNo.getText();
        String cCityState = fieldcCityState.getText();
        String cZipCode = fieldcZipCode.getText();
        
        String pStreet = fieldpStreetName.getText();
        String pAptNo = fieldpAptNo.getText();
        String pCityState = fieldpCityState.getText();
        String pZipCode = fieldpZipCode.getText();
        
        String pPhNo = fieldpPhNo.getText();
        String pEmail = fieldpEmail.getText();
        
        String oPhNo = fieldoPhNo.getText();
        String oEmail = fieldoEmail.getText();
        
        this.person.setFirstName(firstName);
        this.person.setLastName(lastName);
        this.person.setCollegeName(collegeName);
        this.person.setnUID(nUID);

        //setting the cuurent Address object.
        Address currentAddress = this.person.getCurrentAddress();
        currentAddress.setStreetName(cStreet);
        currentAddress.setAptNo(cAptNo);
        currentAddress.setCityState(cCityState);
        currentAddress.setZipCode(cZipCode);

        //setting the permanent address object.
        Address permanentAddress = this.person.getPermanentAddress();
        permanentAddress.setStreetName(pStreet);
        permanentAddress.setAptNo(pAptNo);
        permanentAddress.setCityState(pCityState);
        permanentAddress.setZipCode(pZipCode);
        
        ContactInformation officeContact = this.person.getOfficeContact();
        officeContact.setPhoneNumber(oPhNo);
        officeContact.setEmail(oEmail);
        
        ContactInformation personalContact = this.person.getPersonalContact();
        personalContact.setPhoneNumber(pPhNo);
        personalContact.setEmail(pEmail);
        

        JOptionPane.showMessageDialog(null, "Saved Successfully!");

        //method to display information on panel.
        display();
    }//GEN-LAST:event_saveButtonActionPerformed

